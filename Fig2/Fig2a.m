clear; clc; rng('default'); rng(0); close all; tic;

mkdir('.\Data'); mkdir('.\Figure')
syms x eta

alpha = 0.5;
c = 0.2;
K = 1;
delta = 1/5;
eqn = alpha * x * (x - c) * (1 - x / K) - delta * eta * x^2;
solution = solve(eqn);

x0 = solution(1);
x_low = solution(2);
x_high = solution(3);
eta_eff_c = ((K + c -2 * sqrt(K * c)) * alpha) / (K * delta);
R_eff_c = double(subs(x_low, eta_eff_c));

t_lin = linspace(0, eta_eff_c, 1e3);
x_low_lin = zeros(1,length(t_lin));
x_high_lin = x_low_lin;
for i = 1:length(t_lin)
    x_low_lin(i) = double(subs(x_low, t_lin(i)));
    x_high_lin(i) = double(subs(x_high, t_lin(i)));
end
x_low_lin(end+1) = -0.05;
x_low_lin(end+1) = -0.05;
x_high_lin(end+1) = 1.1;
x_high_lin(end+1) = 1.1;
t_lin(end+1) = t_lin(end);
t_lin(end+1) = 0;
t_low_high = [t_lin(1:end-2), t_lin(end-2:-1:1)];
x_low_high = [x_low_lin(1:end-2), x_high_lin(end-2:-1:1)];

t_0 = linspace(0, 1, 100);
x_0 = x0 * ones(100, 1);

